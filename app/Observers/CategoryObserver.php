    /**
     * Handle the Category "deleted" event.
     *
     * @param  \App\Models\Category  $category
     * @return void
     */
    public function deleted(Category $category)
    {
        $tips = Tip::where('category_id',$category->id)->get();
        if(count($tips) > 0)
        {
            foreach($tips as $tip)
            {
                unlink(public_path($tip->team_one_image));
                unlink(public_path($tip->team_two_image));
                $tip->delete();
            }
        }

    }

